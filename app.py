import streamlit as st
import pandas as pd
import pickle

st.title("Salary Prediction:heavy_dollar_sign:")

model = pickle.load(open('model.pkl', 'rb'))
experience = st.number_input("Years of Experience",1,10)
test = st.number_input("Test Score",1,10)
interview = st.number_input("Interview Score",1,10)
if st.button("Predict"):
  result = model.predict([[experience, test, interview]])
  result = round(result[0],2)
  st.write(f"Predicted Salary: {result}")





